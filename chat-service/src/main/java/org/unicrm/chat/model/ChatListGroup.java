package org.unicrm.chat.model;

public class ChatListGroup {
    private Long id;
    private String title;

    public ChatListGroup() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
